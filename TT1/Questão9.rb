#Quais as vantagens e desvantagens do ruby ser uma linguagem dinamicamente tipada?

#As vantagens são:

Flexibilidade, agilidade e código mais limpo.
Excelente para prototipagem e desenvolvimento rápido.
Facilidade com metaprogramação.

#As desvantagens são:

Maior chance de erros de tipo em tempo de execução.
Pode ser desafiador para manutenção de grandes projetos.
Desempenho inferior em comparação com linguagens estaticamente tipadas.
